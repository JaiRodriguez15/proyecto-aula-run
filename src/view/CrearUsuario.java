    private void LblCrearMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_LblCrearMouseClicked
        String user = txtCreateUser.getText().trim();
        String pass = txtCreatePass.getText().trim();
        String palSeguridad = txtCreatePS.getText().trim();
        
        
        

        if (user.equals("Nombre de Usuario") || pass.equals("Contrase√±a") || palSeguridad.equals("Palabra de Seguridad")) {
            JOptionPane.showMessageDialog(null, "Por favor, complete todos los campos.");
            return;
        }
    }//GEN-LAST:event_LblCrearMouseClicked

